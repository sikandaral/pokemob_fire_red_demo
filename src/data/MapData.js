const G = GameConfig.TILES;

function makeGrid(width, height, fill = G.GRASS) {
    return Array.from({ length: height }, (_, y) =>
        Array.from({ length: width }, (_, x) =>
            x === 0 || y === 0 || x === width - 1 || y === height - 1 ? G.WALL : fill
        )
    );
}

function setRect(tiles, x, y, rectWidth, rectHeight, tile) {
    for (let dy = 0; dy < rectHeight; dy += 1) {
        for (let dx = 0; dx < rectWidth; dx += 1) {
            if (tiles[y + dy]?.[x + dx] != null) {
                tiles[y + dy][x + dx] = tile;
            }
        }
    }
}

function setTile(tiles, x, y, tile) {
    if (tiles[y]?.[x] != null) {
        tiles[y][x] = tile;
    }
}

function makeCoordRange(y, startX, endX) {
    const coords = [];
    for (let x = startX; x <= endX; x += 1) {
        coords.push({ x, y });
    }
    return coords;
}

function makeTilesFromCollisionRows(rows, grassZones = [], doorCoords = []) {
    const tiles = rows.map((row) => row.split('').map((char) => (char === '#' ? G.WALL : G.PATH)));

    grassZones.forEach((zone) => {
        for (let y = zone.y; y < zone.y + zone.height; y += 1) {
            for (let x = zone.x; x < zone.x + zone.width; x += 1) {
                if (tiles[y]?.[x] !== G.WALL) {
                    tiles[y][x] = G.GRASS;
                }
            }
        }
    });

    doorCoords.forEach(({ x, y }) => setTile(tiles, x, y, G.DOOR));
    return tiles;
}

function makeTilesFromCollisionAndGrassRows(collisionRows, grassRows, doorCoords = []) {
    const tiles = collisionRows.map((row, y) => row.split('').map((char, x) => {
        if (char === '#') {
            return G.WALL;
        }

        return grassRows[y]?.[x] === 'G' ? G.GRASS : G.PATH;
    }));

    doorCoords.forEach(({ x, y }) => setTile(tiles, x, y, G.DOOR));
    return tiles;
}

